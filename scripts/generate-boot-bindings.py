def sha256(data: bytes) -> bytes:
    return hashlib.sha256(data).digest()


def generation_identity(data: bytes) -> bytes:
    return sha256(data[:24] + bytes(32) + data[56:])


def bootstate_checksum(slot: bytes) -> bytes:
    return sha256(slot[:BOOTSTATE_CHECKSUM_OFFSET] + bytes(32) + slot[BOOTSTATE_CHECKSUM_END:])


def bootstore_checksum(data: bytes) -> bytes:
    offset = BOOTSTORE_DIRECTORY_OFFSET + 48
    return sha256(data[BOOTSTATE_SLOT_BYTES * 2 : offset] + bytes(32) + data[offset + 32 :])
