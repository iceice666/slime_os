def check_contracts() -> None:
    environment = os.environ.copy()
    environment["ZUTAI_STDLIB_ROOT"] = str(STDLIB)
    for contract in CONTRACTS:
        for name in ("schema.zt", "gen_rust.zt"):
            subprocess.run(
                [
                    "cargo", "run", "--manifest-path", str(ROOT / "deps" / "zutai" / "Cargo.toml"),
                    "-q", "-p", "zutai-cli", "--", "check", str(contract / name),
                ],
                cwd=ROOT,
                env=environment,
                check=True,
            )


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--check", action="store_true")
    args = parser.parse_args()
    check_contracts()
    if args.check:
        if not OUTPUT.exists() or OUTPUT.read_text(encoding="utf-8") != SOURCE:
            raise SystemExit("generated boot bindings are stale; run `just boot_gen`")
        print("Boot contract bindings are current")
        return
    OUTPUT.write_text(SOURCE, encoding="utf-8")
    print(f"Generated {OUTPUT.relative_to(ROOT)}")


if __name__ == "__main__":
    main()
